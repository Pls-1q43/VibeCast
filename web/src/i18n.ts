export const LANGUAGES = [
  { code: "zh-CN", label: "中文" },
  { code: "en", label: "English" },
  { code: "ja", label: "日本語" },
  { code: "ko", label: "한국어" },
  { code: "es", label: "Español" },
  { code: "hi", label: "हिन्दी" },
  { code: "ar", label: "العربية" },
] as const;

export type Lang = (typeof LANGUAGES)[number]["code"];

export const DEFAULT_LANG: Lang = "zh-CN";
const STORAGE_KEY = "vibecast.lang";

const aliases: Record<string, Lang> = {
  zh: "zh-CN",
  "zh-cn": "zh-CN",
  "zh-hans": "zh-CN",
  en: "en",
  ja: "ja",
  jp: "ja",
  ko: "ko",
  kr: "ko",
  es: "es",
  hi: "hi",
  ar: "ar",
};

export function normalizeLang(value: string | null | undefined): Lang | null {
  if (!value) return null;
  const lower = value.toLowerCase();
  if (aliases[lower]) return aliases[lower];
  const prefix = lower.split("-")[0];
  return aliases[prefix] ?? null;
}

export function currentLang(): Lang {
  const fromUrl = normalizeLang(new URLSearchParams(location.search).get("lang"));
  if (fromUrl) {
    localStorage.setItem(STORAGE_KEY, fromUrl);
    applyDocumentLocale(fromUrl);
    return fromUrl;
  }
  const fromStorage = normalizeLang(localStorage.getItem(STORAGE_KEY));
  if (fromStorage) {
    applyDocumentLocale(fromStorage);
    return fromStorage;
  }
  const fromNavigator = normalizeLang(navigator.language);
  const lang = fromNavigator ?? DEFAULT_LANG;
  applyDocumentLocale(lang);
  return lang;
}

export function setLang(lang: Lang): void {
  localStorage.setItem(STORAGE_KEY, lang);
  const url = new URL(location.href);
  url.searchParams.set("lang", lang);
  history.replaceState(null, "", url);
  applyDocumentLocale(lang);
}

export function applyDocumentLocale(lang: Lang): void {
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
}

export function langName(lang: Lang): string {
  return LANGUAGES.find((l) => l.code === lang)?.label ?? lang;
}

