const dictionaries: Record<Lang, Dict> = { "zh-CN": zh, en, ja, ko, es, hi, ar };

export function createI18n(lang = currentLang()) {
  return {
    lang,
    t(key: string, values: Record<string, string | number> = {}): string {
      const template = dictionaries[lang][key] ?? dictionaries[DEFAULT_LANG][key] ?? key;
      return template.replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? ""));
    },
    status(status: SyncStatus): string {
      return dictionaries[lang][`status.${status}`] ?? dictionaries[DEFAULT_LANG][`status.${status}`] ?? status;
    },
    error(code?: string | null, fallback?: string | null): string {
      if (code) {
        const direct = dictionaries[lang][`err.${code}`];
        if (direct) return direct;
        const generic = dictionaries[lang]["err.generic"] ?? dictionaries[DEFAULT_LANG]["err.generic"] ?? "{code}";
        return generic.replace(/\{code\}/g, code);
      }
      return fallback ?? "";
    },
  };
}

export type I18n = ReturnType<typeof createI18n>;
